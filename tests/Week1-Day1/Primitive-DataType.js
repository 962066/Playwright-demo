/*Declare variables using `let` for different data types in JavaScript, and verify their types using `typeof`,
including an uninitialized variable.*/

//userName // String
let userName = "Yuvraj";
console.log(typeof userName); // Output: string

//organization // String 
let organization = "Testleaf";
console.log(typeof organization); // Output: string

//contactNumber // Number
let contactNumber = 9620669785;
console.log(typeof contactNumber); // Output: number

//knowsAutomation // Boolean 
let knowsAutomation = true;
console.log(typeof knowsAutomation); // Output: boolean 

//usesPlaywright // Undefined (not assigned)
